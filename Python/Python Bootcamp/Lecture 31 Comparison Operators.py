"""
Comparison Operators
"""

print(2 == 2)
# Output: True

print(1 == 1)
# Output: True

print(1 == 0)
# Output: False

# Not Equal

print(2 != 1)
# Output: True

print(2 != 2)
# Output: False

# Greater Than / Less Than

print(2 > 1)
# Output: True

print(2 > 4)
# Output: False

print(2 < 1)
# Output: False

print(2 >= 2)
# Output: True

print(2 >= 1)
# Output: True

print(2 <= 2)
# Output: True

print(2 <= 10)
# Output: True